package com.zyphorabrowser;

/**
 * Represents the different browsing modes for Zyphora Browser.
 */
public enum BrowserMode {
    /**
     * A full, feature-rich browsing experience for high-speed connections.
     */
    FULL_BROWSER,

    /**
     * An ultra-lite, text-only or HTML-only mode for very slow connections (< 10kbps).
     */
    ULTRA_LITE
}
